public class Bonus {
    public int x;
    public int y;
    public int points;

    public Bonus(int x, int y, int points) {
        this.x = x;
        this.y = y;
        this.points = points;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getPoints() {
        return points;
    }
}